package assisted_project_no20_program;

interface First 
{  
    default void show() 
    { 
        System.out.println("Default First"); 
    } 
} 
interface Second 
{  
    default void show() 
    { 
        System.out.println("Default Second"); 
    } 
}  


public class DimondDemo implements First, Second
{
	 
	    public void show() 
	    {  
	        First.super.show(); 
	        Second.super.show(); 
	    } 
	    public static void main(String args[]) 
	    { 
	        DimondDemo ob = new DimondDemo(); 
	        ob.show(); 
	    } 

}
